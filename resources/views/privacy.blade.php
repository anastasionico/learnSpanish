				<h2 class="c-greenLight"><span id="Summary">Summary</span></h2>
				<p>If you’re collecting any sort of <strong>personal information</strong> from your website’s visitors, end users, subscribers, customers, or clients then you’re most likely <strong>required by law or by third party services</strong> to post a Privacy Policy on your website.</p>
				<p>You need to be aware of:</p>
				<ul>
				<li>The privacy laws and Privacy Policy requirements in the <strong>jurisdiction</strong> your business is based in and the jurisdiction(s) where your site visitors live.</li>
				<li>The Terms of Service requirements of any <strong>third party services</strong> your website or app uses.</li>
